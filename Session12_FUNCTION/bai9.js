//và được gọi lại sau khi hoạt động đã hoàn thành hoặc điều kiện đã đạt được.
//Callback function giúp người dùng đảm bảo rằng một code sẽ vẫn được thực thi khi code khác đang tiếp diễn mà không phải chờ đợi.




//Chức năng của callback function:
//1.Xử lý bất đồng bộ: 
//Callback functions thường được sử dụng để xử lý các hoạt động bất đồng bộ như gọi API, đọc tệp, hoặc xử lý sự kiện. 
//2. Xử lý kết quả của một hoạt động: 
//Callback functions thường được sử dụng để xử lý kết quả của một hoạt động. Ví dụ, sau khi gọi API, callback function có thể được sử dụng để xử lý dữ liệu trả về từ API đó.
//3.Thực hiện logic điều kiện:
// Callback functions có thể được sử dụng để thực hiện logic điều kiện, chẳng hạn như kiểm tra một điều kiện và thực hiện hành động tùy thuộc vào kết quả của điều kiện đó.

//Cách hoạt động của callback function trong JavaScript:

//1.Truyền hàm như một đối số: 
//Callback function được truyền vào một hàm khác như một đối số.

//2.Gọi lại hàm: 
//Hàm gốc sẽ gọi lại callback function sau khi hoạt động của nó hoàn thành hoặc điều kiện đã đạt được.

//3.Thực hiện logic trong callback function: 
//Callback function được gọi lại và thực hiện logic được định nghĩa bên trong nó.